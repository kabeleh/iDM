# This script
# First, extract the model parameters from the passed *.bestfit file
# Second, runs CLASS with those parameters to obtain:
#   - The scalar field evolution
#   - The scalar field potential evolution including the first and second derivatives
#   - The equation of state evolution of the scalar field
#   - The dark matter and dark energy density evolution
#   - The matter power spectrum P(k)
#   - The lensed CMB angular power spectrum C_ℓ
# Third, plots
#   - the de Sitter conjecture |dV|/V, |d^2V|/V, and the equation of state w as a function of redshift z
#   - the dark matter and dark energy density evolution as a function of redshift z
#   - the EoS evolution as a function of redshift z to check for phantom crossing
#   - 1+w-0.15s_1^2 as a function of redshift z to check for the swampland conjecture
#   - the matter power spectrum P(k) as a function of wavenumber k (log-log scale)
#   - the lensed CMB power spectrum C_ℓ (TT, EE, TE) as a function of multipole ℓ (log-log scale)

# Usage:
# python BestFitPlot.py --bestfit_file path/to/bestfit_file.bestfit --output_dir path/to/output_dir
# The bestfit_file should be a .bestfit file generated by the MCMC analysis, containing the best-fit parameters for the model.
# The output_dir is the directory where the plots will be saved.
# Default ouput dir is the current directory.
# --reuse_background can be used to skip rerunning CLASS and reuse existing output files and cache, e.g. to just regenerate the plots.

# 1. Extract the model parameters from the passed *.bestfit file

"""
BestFitPlot.py: Extract best-fit parameters from MCMC chains and prepare them for CLASS.

This script:
1. Extracts model parameters from a *.bestfit file (Cobaya output format)
2. Loads the corresponding *.input.yaml file (Cobaya MCMC configuration)
3. Maps Cobaya/MCMC parameter names to CLASS parameter names
4. Combines special parameters (e.g., scalar field parameters) into CLASS-compatible strings
5. Runs CLASS to compute background evolution, P(k), and C_ℓ outputs
6. Optionally loads LCDM baseline background data and computes Omega_cdm and Omega_λ
7. Generates five publication-quality plots:
   - Background evolution quantities (de Sitter parameters, energy densities, swampland)
   - Energy density plot with optional ΔΩ_cdm and ΔΩ_DE evolution (when LCDM baseline is provided)
   - Matter power spectrum P(k) with optional LCDM overlay
   - Lensed CMB power spectrum C_ℓ (TT, EE, TE modes) with optional LCDM overlay

Usage:

    # Basic usage: analyze model best-fit in current directory
    python BestFitPlot.py --bestfit_file /path/to/model.bestfit

    # Specify output directory
    python BestFitPlot.py --bestfit_file /path/to/model.bestfit --output_dir /path/to/output

    # Reuse existing CLASS outputs and cached data (fast iteration on plots)
    python BestFitPlot.py --bestfit_file /path/to/model.bestfit --reuse_background

    # Compare model against LCDM baseline:
    # - Runs CLASS for model best-fit parameters (or reuses if --reuse_background is set)
    # - Extracts LCDM background.dat and computes Omega_cdm^LCDM and Omega_λ^LCDM
    # - Overlays LCDM in P(k) and C_ℓ plots
    # - In energy density plot: adds lower panel showing ΔΩ_cdm and ΔΩ_DE evolution
    python BestFitPlot.py --bestfit_file /path/to/model.bestfit --lcdm_baseline /path/to/lcdm.bestfit --output_dir /path/to/output

Arguments:
    --bestfit_file BESTFIT_FILE
        Path to the .bestfit file (Cobaya MCMC output). Required.
        Must have a corresponding .input.yaml file with the same base name.

    --output_dir OUTPUT_DIR
        Directory where results and plots are saved (default: current directory).

    --reuse_background
        Skip running CLASS for the model and reuse existing output files and caches.
        Useful for iterating on plot aesthetics without recomputing CLASS spectra.

    --lcdm_baseline LCDM_BASELINE
        Optional path to an LCDM .bestfit file. When provided:
        - Generates or reuses LCDM background.dat, pk_matter.dat, cl_lensed.dat
        - Extracts Ω_cdm^LCDM and Ω_λ^LCDM from the LCDM background data
        - Computes ΔΩ_cdm = Ω_cdm^{model} - Ω_cdm^{LCDM}
        - Computes ΔΩ_DE = Ω_scf^{model} - Ω_λ^{LCDM}
        - Overlays LCDM curves in P(k) and C_ℓ plots
        - Extends energy-density plot (plot 2) into a two-panel figure:
          * Upper panel: model Ω_scf and Ω_cdm (unchanged from single-panel case)
          * Middle axis: shared redshift z scale
          * Lower panel: ΔΩ_cdm and ΔΩ_DE evolution with reference at Δ=0

Output Files:
    For each plot, three formats are generated: PNG, PDF, PGF
    - plot1_*: de Sitter parameters (s_1, s_2)
    - plot2_*: Energy densities (Ω_scf, Ω_cdm; or with ΔΩ panel if --lcdm_baseline is set)
    - plot3_*: Swampland expression (1+w-0.15s₁²) and equation of state (w)
    - plot4_*: Matter power spectrum P(k) [+ LCDM overlay if --lcdm_baseline]
    - plot5_*: Lensed CMB power spectrum C_ℓ (TT, EE, TE) [+ LCDM overlay if --lcdm_baseline]

    Plus cache files for fast reruns:
    - *.processed.npz: cached CLASS output datasets
    - *.lcdm_omegas.processed.npz: cached LCDM Omega_cdm and Omega_lambda values
"""

