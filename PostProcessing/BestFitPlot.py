def extract_class_parameters(
    bestfit_values: Dict[str, float], yaml_config: Dict[str, Any]
) -> Dict[str, Any]:
    """Map Cobaya MCMC parameters to CLASS input parameters.

    This function:
    1. Extracts parameters marked as drop: false and derived: false (or no 'derived' key)
    2. Handles special scalar field parameters that need to be combined into 'scf_parameters'
    3. Includes extra_args from the YAML theory.classy section
    4. Filters to only include known CLASS input parameters

    Args:
        bestfit_values: Parameter values extracted from .bestfit file
        yaml_config: Parsed YAML configuration from .input.yaml

    Returns:
        Dictionary ready to pass to classy.set(), with CLASS parameter names and values

    Raises:
        KeyError: If required sections are missing from the YAML
        ValueError: If parameter extraction fails
    """
    class_params = {}

    # 1. Extract theory.classy.extra_args (these are trusted CLASS inputs)
    if "theory" in yaml_config and "classy" in yaml_config["theory"]:
        classy_config = yaml_config["theory"]["classy"]
        if "extra_args" in classy_config:
            class_params.update(classy_config["extra_args"])

    # 2. Extract primary (non-dropped, non-derived) parameters from params section
    if "params" not in yaml_config:
        raise KeyError("'params' section not found in YAML configuration")

    params_config = yaml_config["params"]
    dropped_scf_params = {}  # Store dropped scalar field parameters for recombination

    for param_name, param_spec in params_config.items():
        if param_name in _NON_INPUT_PARAMETERS:
            continue

        # Skip output/tracking parameters that are not CLASS inputs
        if param_name not in _CLASS_INPUT_PARAMETERS and not param_name.startswith(
            "scf_"
        ):
            continue

        # Skip if parameter specification is a scalar value (like Omega_fld = 0.0)
        if not isinstance(param_spec, dict):
            # Handle direct scalar assignments (e.g., Omega_fld: 0.0)
            if param_name in _CLASS_INPUT_PARAMETERS:
                class_params[param_name] = param_spec
            continue

        # Determine if this parameter should be included in CLASS input
        is_dropped = param_spec.get("drop", False)
        has_derived = "derived" in param_spec

        # Special handling for scf_parameters: it should be constructed, not directly copied
        if param_name == "scf_parameters":
            # This will be handled separately after collecting all needed parameters
            continue

        # Extract parameter value from bestfit if it exists
        if param_name in bestfit_values:
            value = bestfit_values[param_name]

            # Collect dropped scalar field parameters for later recombination
            if is_dropped and param_name.startswith("scf_"):
                dropped_scf_params[param_name] = value

            # Include parameter if:
            # 1. It's not dropped and not derived, OR
            # 2. It's a dropped scalar field parameter (for later reconstruction)
            if not is_dropped and not has_derived:
                class_params[param_name] = value
            elif is_dropped and param_name.startswith("scf_"):
                # Keep dropped scalar field params for reconstruction
                dropped_scf_params[param_name] = value
        elif not is_dropped and not has_derived and param_name in params_config:
            # If parameter spec has a 'value' field, use it
            if isinstance(param_spec, dict) and "value" in param_spec:
                class_params[param_name] = param_spec["value"]

    # 3. Reconstruct scf_parameters from individual components
    if dropped_scf_params and "chi_ini" in bestfit_values:
        class_params["scf_parameters"] = _construct_scf_parameters(
            dropped_scf_params=dropped_scf_params,
            bestfit_values=bestfit_values,
            yaml_config=yaml_config,
        )

    # 4. CLASS expects yes/no strings for boolean-like flags.
    for key, value in list(class_params.items()):
        if isinstance(value, bool):
            class_params[key] = "yes" if value else "no"

    return class_params


def _construct_scf_parameters(
    dropped_scf_params: Dict[str, float],
    bestfit_values: Dict[str, float],
    yaml_config: Dict[str, Any],
) -> str:
    """Construct the scf_parameters string from individual scalar field parameters.

    The scf_parameters string is a comma-separated list of CLASS scalar field parameters
    in the order: c1, c2, c3, c4, q1, q2, q3, q4, exp1, exp2, phi_ini, phi_prime_ini

        Values are resolved with the following precedence:
            1. Explicit value present in the .bestfit file (preferred)
            2. Value captured in dropped_scf_params
            3. Literal YAML default value (if provided)
            4. Fallback to 0.0

        This precedence is important because some chains provide derived scf_* entries
        (e.g. scf_c1, scf_phi_ini) in the .bestfit file without a literal YAML value.

    Args:
        dropped_scf_params: Individual scalar field parameters from .bestfit
        bestfit_values: All parameter values from .bestfit
        yaml_config: YAML configuration for extracting default values if needed

    Returns:
        A comma-separated string of scalar field parameters for CLASS
    """
    # Define the expected order of parameters in scf_parameters
    scf_param_order = [
        "scf_c1",
        "scf_c2",
        "scf_c3",
        "scf_c4",
        "scf_q1",
        "scf_q2",
        "scf_q3",
        "scf_q4",
        "scf_exp1",
        "scf_exp2",
        "scf_phi_ini",
        "scf_phi_prime_ini",
    ]

    scf_values = []

    for scf_param in scf_param_order:
        # Prefer the actual best-fit number when available.
        if scf_param in bestfit_values:
            scf_values.append(str(bestfit_values[scf_param]))
            continue

        # Fall back to collected dropped entries.
        if scf_param in dropped_scf_params:
            scf_values.append(str(dropped_scf_params[scf_param]))
            continue

        # Fall back to literal YAML defaults when provided.
        if (
            "params" in yaml_config
            and scf_param in yaml_config["params"]
            and isinstance(yaml_config["params"][scf_param], dict)
            and "value" in yaml_config["params"][scf_param]
        ):
            scf_values.append(str(yaml_config["params"][scf_param]["value"]))
            continue

        # Last resort fallback.
        scf_values.append("0.0")

    return ",".join(scf_values)


def extract_parameters_from_bestfit(
    bestfit_path: str, yaml_path: Optional[str] = None
) -> Tuple[Dict[str, Any], Dict[str, Any]]:
    """Main function: Extract CLASS parameters from Cobaya best-fit and YAML files.

    This is the primary entry point that orchestrates the entire parameter extraction process.

    Args:
        bestfit_path: Path to the .bestfit file
        yaml_path: Path to the .input.yaml file (auto-detected if None)

    Returns:
        Tuple containing:
            - Dictionary of CLASS parameters ready for classy.set()
            - Dictionary of extraction metadata and warnings

    Raises:
        FileNotFoundError: If required files are not found
        ValueError: If extraction fails

    Example:
        >>> class_params, metadata = extract_parameters_from_bestfit(
        ...     'hyperbolic_Planck_InitCond_MCMC.bestfit'
        ... )
        >>> print(class_params)
        {'H0': 67.32, 'n_s': 0.964, ..., 'scf_parameters': '1.037e-07,0.5,0.0,...'}
    """
    # Find YAML file if not provided
    if yaml_path is None:
        yaml_path = find_input_yaml(bestfit_path)

    # Load files
    bestfit_values = load_bestfit_file(bestfit_path)
    yaml_config = load_input_yaml(yaml_path)

    # Extract CLASS parameters
    class_params = extract_class_parameters(bestfit_values, yaml_config)

    # Prepare metadata
    metadata = {
        "bestfit_file": os.path.abspath(bestfit_path),
        "yaml_file": os.path.abspath(yaml_path),
        "parameters_extracted": len(class_params),
        "warnings": [],
    }

    return class_params, metadata


def extract_lcdm_baseline_parameters(
    bestfit_path: str, yaml_path: Optional[str] = None
) -> Tuple[Dict[str, Any], Dict[str, Any]]:
    """Extract a minimal CLASS parameter set for an LCDM baseline run."""
    if yaml_path is None:
        yaml_path = find_input_yaml(bestfit_path)

    bestfit_values = load_bestfit_file(bestfit_path)
    yaml_config = load_input_yaml(yaml_path)

    class_params: Dict[str, Any] = {}

    if "theory" in yaml_config and "classy" in yaml_config["theory"]:
        classy_config = yaml_config["theory"]["classy"]
        if "extra_args" in classy_config and isinstance(
            classy_config["extra_args"], dict
        ):
            class_params.update(classy_config["extra_args"])

    for key in _LCDM_BASELINE_INPUT_KEYS:
        if key in bestfit_values:
            class_params[key] = bestfit_values[key]

    # Recover A_s when only logA is present.
    if "A_s" not in class_params and "logA" in bestfit_values:
        class_params["A_s"] = 1e-10 * np.exp(bestfit_values["logA"])

    for forbidden in _DISALLOWED_LCDM_KEYS:
        class_params.pop(forbidden, None)

    for key, value in list(class_params.items()):
        if isinstance(value, bool):
            class_params[key] = "yes" if value else "no"

    metadata = {
        "bestfit_file": os.path.abspath(bestfit_path),
        "yaml_file": os.path.abspath(yaml_path),
        "parameters_extracted": len(class_params),
        "warnings": [],
    }

    return class_params, metadata


def _build_output_paths(bestfit_path: str, output_dir: str) -> Dict[str, str]:
    """Build canonical output paths for a best-fit file label."""
    run_label = Path(bestfit_path).name.replace(".bestfit", "")
    output_root = Path(output_dir).expanduser().resolve() / run_label

    return {
        "output_root": str(output_root),
        "background_file": str(Path(f"{output_root}_background.dat")),
        "pk_file": str(Path(f"{output_root}_pk.dat")),
        "cl_lensed_file": str(Path(f"{output_root}_cl_lensed.dat")),
    }


def resolve_or_run_class_outputs(
    class_params: Dict[str, Any],
    bestfit_path: str,
    output_dir: str,
    *,
    reuse_only: bool,
    reuse_if_available: bool,
) -> Dict[str, str]:
    """Resolve CLASS outputs by reusing existing files or running CLASS as needed."""
    candidate = _build_output_paths(bestfit_path=bestfit_path, output_dir=output_dir)
    bg_ok = Path(candidate["background_file"]).exists()
    pk_ok = Path(candidate["pk_file"]).exists()
    cl_ok = Path(candidate["cl_lensed_file"]).exists()
    all_ok = bg_ok and pk_ok and cl_ok

    if reuse_only:
        if not all_ok:
            raise FileNotFoundError(
                "--reuse_background was set but one or more output files do not exist:\n"
                f"  Background: {bg_ok}\n"
                f"  P(k):       {pk_ok}\n"
                f"  C_ℓ:        {cl_ok}"
            )
        return {
            "class_executable": str(Path(__file__).resolve().parents[1] / "class"),
            "generated_ini": "(reused)",
            "output_root": candidate["output_root"],
            "background_file": candidate["background_file"],
            "pk_file": candidate["pk_file"],
            "cl_lensed_file": candidate["cl_lensed_file"],
            "run_source": "reused",
        }

    if reuse_if_available and all_ok:
        return {
            "class_executable": str(Path(__file__).resolve().parents[1] / "class"),
            "generated_ini": "(reused)",
            "output_root": candidate["output_root"],
            "background_file": candidate["background_file"],
            "pk_file": candidate["pk_file"],
            "cl_lensed_file": candidate["cl_lensed_file"],
            "run_source": "reused",
        }

    run_info = run_class_background(
        class_params=class_params,
        bestfit_path=bestfit_path,
        output_dir=output_dir,
    )
    run_info["run_source"] = "computed"
    return run_info


def _serialize_class_value(value: Any) -> str:
    """Serialize a Python value into a CLASS .ini-compatible value string."""
    if isinstance(value, bool):
        return "yes" if value else "no"
    if value is None:
        return ""
    return str(value)


