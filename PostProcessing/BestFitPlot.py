# 2.c) Extract P(k) power spectrum data.


def load_pk_dataset(pk_file: str) -> Dict[str, Any]:
    """Load CLASS P(k) file and extract k and P(k) values.

    Extracts:
      - k (wavenumber in h/Mpc)
      - P_k (matter power spectrum in (Mpc/h)^3)

    Args:
        pk_file: Path to the CLASS *_pk.dat file

    Returns:
        Dictionary containing:
            - k: wavenumber array
            - P_k: power spectrum array
            - pk_file: source file path
    """
    if not Path(pk_file).exists():
        raise FileNotFoundError(f"P(k) file not found: {pk_file}")

    data = np.loadtxt(pk_file, comments="#")

    if data.ndim == 1:
        data = data[np.newaxis, :]

    # CLASS P(k) file format: k, P(k)
    # First column is k, second is P(k)
    if data.shape[1] < 2:
        raise ValueError(f"P(k) file has insufficient columns: {data.shape}")

    k = data[:, 0]
    P_k = data[:, 1]

    return {
        "pk_file": pk_file,
        "k": k,
        "P_k": P_k,
    }


