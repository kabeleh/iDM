def parse_arguments() -> argparse.Namespace:
    """Parse command-line arguments for BestFitPlot.

    Returns:
        argparse.Namespace: Parsed arguments with 'bestfit_file' and 'output_dir'
    """
    parser = argparse.ArgumentParser(
        description="Extract CLASS parameters from Cobaya MCMC best-fit files"
    )
    parser.add_argument(
        "--bestfit_file",
        type=str,
        required=True,
        help="Path to the .bestfit file (Cobaya MCMC output)",
    )
    parser.add_argument(
        "--output_dir",
        type=str,
        default=".",
        help="Output directory for results (default: current directory)",
    )
    parser.add_argument(
        "--reuse_background",
        action="store_true",
        help=(
            "Reuse existing <output_root>_background.dat and cache instead of rerunning CLASS. "
            "Useful while iterating on plotting aesthetics."
        ),
    )
    return parser.parse_args()


def load_bestfit_file(bestfit_path: str) -> Dict[str, float]:
    """Parse a Cobaya .bestfit file and extract parameter values.

    The .bestfit file format contains lines with:
        index  value  parameter_name  LaTeX_description

    Parameters starting with '#' are ignored (comment lines).

    Args:
        bestfit_path: Path to the .bestfit file

    Returns:
        Dictionary mapping parameter names to their float values

    Raises:
        FileNotFoundError: If the file does not exist
        ValueError: If the file format is invalid
    """
    if not os.path.exists(bestfit_path):
        raise FileNotFoundError(f"Best-fit file not found: {bestfit_path}")

    parameters = {}
    with open(bestfit_path, "r") as f:
        for line_num, line in enumerate(f, 1):
            line = line.strip()

            # Skip empty lines and comments
            if not line or line.startswith("#") or line.startswith("-"):
                continue

            # Parse the line: index  value  parameter_name  LaTeX_description
            parts = line.split()
            if len(parts) < 3:
                continue

            try:
                # Extract index, value, and parameter name
                index = int(parts[0])
                value = float(parts[1])
                param_name = parts[2]

                # Store the parameter
                parameters[param_name] = value
            except (ValueError, IndexError):
                # Skip lines that don't match the expected format
                continue

    if not parameters:
        raise ValueError(f"No parameters found in file: {bestfit_path}")

    return parameters


def load_input_yaml(yaml_path: str) -> Dict[str, Any]:
    """Load a Cobaya .input.yaml file (MCMC configuration).

    Args:
        yaml_path: Path to the .input.yaml file

    Returns:
        Parsed YAML dictionary

    Raises:
        FileNotFoundError: If the file does not exist
        yaml.YAMLError: If the YAML is invalid
    """
    if not os.path.exists(yaml_path):
        raise FileNotFoundError(f"Input YAML file not found: {yaml_path}")

    with open(yaml_path, "r") as f:
        config = yaml.safe_load(f)

    return config


def find_input_yaml(bestfit_path: str) -> str:
    """Find the corresponding .input.yaml file for a .bestfit file.

    Assumes the .input.yaml file has the same name as .bestfit but with
    ".input.yaml" extension instead of ".bestfit".

    Args:
        bestfit_path: Path to the .bestfit file

    Returns:
        Path to the corresponding .input.yaml file

    Raises:
        FileNotFoundError: If the .input.yaml file is not found
    """
    base_path = bestfit_path.rsplit(".bestfit", 1)[0]
    yaml_path = f"{base_path}.input.yaml"

    if not os.path.exists(yaml_path):
        raise FileNotFoundError(
            f"Could not find corresponding YAML file: {yaml_path}\n"
            f"Expected base name: {base_path}"
        )

    return yaml_path


