def _prepare_plot_arrays(background_dataset: Dict[str, Any]) -> Dict[str, np.ndarray]:
    """Prepare sorted plotting arrays and derived series used in all figures."""
    z = np.asarray(background_dataset["z"], dtype=float)
    order = np.argsort(z)

    z_plot = z[order]
    s1 = np.asarray(background_dataset["s1"], dtype=float)[order]
    # Convention: dataset stores minus_s2 = V''/V, so s2 = -V''/V = -minus_s2.
    s2 = -np.asarray(background_dataset["minus_s2"], dtype=float)[order]
    omega_scf = np.asarray(background_dataset["Omega_scf"], dtype=float)[order]
    omega_cdm = np.asarray(background_dataset["Omega_cdm"], dtype=float)[order]
    w = np.asarray(background_dataset["w"], dtype=float)[order]
    swampland_expr = np.asarray(background_dataset["swampland_expr"], dtype=float)[
        order
    ]

    return {
        "z": z_plot,
        "s1": s1,
        "s2": s2,
        "Omega_scf": omega_scf,
        "Omega_cdm": omega_cdm,
        "w": w,
        "swampland_expr": swampland_expr,
    }


def _style_redshift_axis(ax: plt.Axes, z: np.ndarray) -> None:
    """Apply consistent redshift-axis styling with z=0 at the right side."""
    finite_z = z[np.isfinite(z)]
    zmax = float(np.nanmax(finite_z)) if finite_z.size else 1.0

    # Symlog preserves visibility near z=0 while handling the full cosmic range.
    ax.set_xscale("symlog", linthresh=1.0)
    ax.set_xlim(zmax, 0.0)

    # Publication-oriented tick design: explicit decade majors plus a terminal 0,
    # and sparse decade minors (2 and 5) for cleaner density.
    if zmax >= 1.0:
        max_pow = int(np.floor(np.log10(zmax)))
        major_ticks = [10.0**p for p in range(max_pow, -1, -1)] + [0.0]
    else:
        major_ticks = [zmax, 0.0]

    major_ticks = sorted(set(float(t) for t in major_ticks))

    major_labels = []
    for t in major_ticks:
        if np.isclose(t, 0.0):
            major_labels.append("0")
        elif t >= 1.0:
            p = int(np.round(np.log10(t)))
            major_labels.append(rf"$10^{{{p}}}$")
        else:
            major_labels.append("")
    ax.xaxis.set_major_locator(FixedLocator(major_ticks))
    ax.xaxis.set_major_formatter(FixedFormatter(major_labels))

    minor_ticks = []
    if zmax >= 10.0:
        for p in range(max_pow - 1, -1, -1):
            d = 10.0**p
            for m in (2, 5):
                t = m * d
                if 1.0 < t < zmax:
                    minor_ticks.append(t)

    # Keep a minimal set of linear-region minor ticks near z~0.
    minor_ticks.extend([0.2, 0.4, 0.6, 0.8])
    minor_ticks = sorted(set(minor_ticks))
    ax.xaxis.set_minor_locator(FixedLocator(minor_ticks))
    ax.xaxis.set_minor_formatter(NullFormatter())

    ax.set_xlabel(r"Redshift $z$")
    ax.grid(True, which="major", alpha=0.25, linewidth=0.6)
    ax.grid(True, which="minor", alpha=0.12, linewidth=0.4)


