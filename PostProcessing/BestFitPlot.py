# 2.d) Extract C_ℓ lensed power spectrum data.


def load_cl_dataset(cl_lensed_file: str, lmax: int = 9000) -> Dict[str, Any]:
    """Load CLASS lensed C_ℓ file and extract ℓ and C_ℓ values for TT, EE, TE.

    Extracts:
      - ell (multipole moments)
      - ell * (ell + 1) / (2 * pi) * C_ℓ^TT (scaled TT power)
      - ell * (ell + 1) / (2 * pi) * C_ℓ^EE (scaled EE power)
      - ell * (ell + 1) / (2 * pi) * C_ℓ^TE (scaled TE power)

    Args:
        cl_lensed_file: Path to the CLASS *_cl_lensed.dat file
        lmax: Maximum multipole to retain (default: 9000)

    Returns:
        Dictionary containing:
            - ell: multipole array
            - ell_times_ell_plus_1_over_2pi: scaling factor ℓ(ℓ+1)/(2π)
            - Cl_TT_scaled: scaled TT power
            - Cl_EE_scaled: scaled EE power
            - Cl_TE_scaled: scaled TE power
            - cl_lensed_file: source file path
    """
    if not Path(cl_lensed_file).exists():
        raise FileNotFoundError(f"C_ℓ file not found: {cl_lensed_file}")

    data = np.loadtxt(cl_lensed_file, comments="#")

    if data.ndim == 1:
        data = data[np.newaxis, :]

    # CLASS lensed C_ℓ file format: ell, TT, EE, TE, BB
    # First column is ell, then TT, EE, TE, BB (if present)
    if data.shape[1] < 4:
        raise ValueError(f"C_ℓ file has insufficient columns: {data.shape}")

    ell = data[:, 0].astype(int)
    Cl_TT = data[:, 1]
    Cl_EE = data[:, 2]
    Cl_TE = data[:, 3]

    # Filter to lmax and apply scaling ℓ(ℓ+1)/(2π)
    mask = ell <= lmax
    ell = ell[mask]
    Cl_TT = Cl_TT[mask]
    Cl_EE = Cl_EE[mask]
    Cl_TE = Cl_TE[mask]

    # Scale by ℓ(ℓ+1)/(2π) for standard power units
    scaling = ell * (ell + 1.0) / (2.0 * np.pi)
    Cl_TT_scaled = scaling * Cl_TT
    Cl_EE_scaled = scaling * Cl_EE
    Cl_TE_scaled = scaling * Cl_TE

    return {
        "cl_lensed_file": cl_lensed_file,
        "ell": ell,
        "ell_times_ell_plus_1_over_2pi": scaling,
        "Cl_TT_scaled": Cl_TT_scaled,
        "Cl_EE_scaled": Cl_EE_scaled,
        "Cl_TE_scaled": Cl_TE_scaled,
    }


def save_cl_dataset_cache(dataset: Dict[str, Any], cache_file: str) -> None:
    """Save processed C_ℓ dataset to a compressed NPZ cache file."""
    path = Path(cache_file)
    path.parent.mkdir(parents=True, exist_ok=True)

    requested_lmax = dataset.get("requested_lmax")
    if requested_lmax is None:
        raise ValueError("C_ℓ cache save requires 'requested_lmax' in dataset")

    np.savez_compressed(
        str(path),
        schema_version=np.array([2], dtype=np.int64),
        cl_lensed_file=np.array([str(dataset["cl_lensed_file"])], dtype=str),
        requested_lmax=np.array([int(requested_lmax)], dtype=np.int64),
        ell=dataset["ell"],
        ell_times_ell_plus_1_over_2pi=dataset["ell_times_ell_plus_1_over_2pi"],
        Cl_TT_scaled=dataset["Cl_TT_scaled"],
        Cl_EE_scaled=dataset["Cl_EE_scaled"],
        Cl_TE_scaled=dataset["Cl_TE_scaled"],
    )


def load_cl_dataset_cache(
    cache_file: str,
    requested_lmax: Optional[int] = None,
) -> Dict[str, Any]:
    """Load processed C_ℓ dataset from a compressed NPZ cache file."""
    with np.load(cache_file, allow_pickle=False) as cached:
        schema_version = int(cached["schema_version"][0])
        if schema_version not in (1, 2):
            raise ValueError(f"Unsupported C_ℓ cache schema version: {schema_version}")

        if schema_version == 2:
            cache_lmax = int(cached["requested_lmax"][0])
        else:
            # Legacy schema has no explicit lmax metadata; infer from stored ell grid.
            cache_lmax = int(np.max(cached["ell"])) if cached["ell"].size else -1

        cache_needs_rewrite = schema_version < 2
        if requested_lmax is not None and cache_lmax != int(requested_lmax):
            raise ValueError(
                f"C_ℓ cache lmax mismatch: cache={cache_lmax}, requested={requested_lmax}"
            )

        return {
            "cl_lensed_file": str(cached["cl_lensed_file"][0]),
            "requested_lmax": cache_lmax,
            "ell": cached["ell"],
            "ell_times_ell_plus_1_over_2pi": cached["ell_times_ell_plus_1_over_2pi"],
            "Cl_TT_scaled": cached["Cl_TT_scaled"],
            "Cl_EE_scaled": cached["Cl_EE_scaled"],
            "Cl_TE_scaled": cached["Cl_TE_scaled"],
            "_cache_needs_rewrite": cache_needs_rewrite,
        }


def load_or_compute_cl_dataset(
    cl_lensed_file: str,
    cache_file: Optional[str] = None,
    lmax: int = 9000,
) -> Tuple[Dict[str, Any], str]:
    """Load processed C_ℓ dataset from cache when valid; otherwise compute and cache.

    Returns:
        (dataset, source) where source is one of: "cache" or "computed".
    """
    if cache_file is None:
        cache_file = str(Path(cl_lensed_file).with_suffix(".processed.npz"))

    cl_path = Path(cl_lensed_file)
    cache_path = Path(cache_file)

    if cache_path.exists() and cache_path.stat().st_mtime >= cl_path.stat().st_mtime:
        try:
            dataset = load_cl_dataset_cache(
                str(cache_path),
                requested_lmax=lmax,
            )
            cache_needs_rewrite = bool(dataset.pop("_cache_needs_rewrite", False))
            if cache_needs_rewrite:
                save_cl_dataset_cache(dataset, str(cache_path))
            return dataset, "cache"
        except (KeyError, ValueError, OSError, EOFError, BadZipFile):
            pass

    dataset = load_cl_dataset(cl_lensed_file, lmax=lmax)
    dataset["requested_lmax"] = int(lmax)
    save_cl_dataset_cache(dataset, str(cache_path))
    return dataset, "computed"


