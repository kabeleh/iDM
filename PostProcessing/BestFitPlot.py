# First, extract the model parameters from the passed *.bestfit file
# Second, runs CLASS with those parameters to obtain:
#   - The scalar field evolution
#   - The scalar field potential evolution including the first and second derivatives
#   - The equation of state evolution of the scalar field
#   - The dark matter and dark energy density evolution
# Third, plots
#   - the de Sitter conjecture |dV|/V, |d^2V|/V, and the equation of state w as a function of redshift z
#   - the dark matter and dark energy density evolution as a function of redshift z
#   - the EoS evolution as a function of redshift z to check for phantom crossing
#   - 1+w-0.15s_1^2 as a function of redshift z to check for the swampland conjecture

# Usage:
# python BestFitPlot.py --bestfit_file path/to/bestfit_file.bestfit --output_dir path/to/output_dir
# The bestfit_file should be a .bestfit file generated by the MCMC analysis, containing the best-fit parameters for the model.
# The output_dir is the directory where the plots will be saved.
# Default ouput dir is the current directory.
# --reuse_background can be used to skip rerunning CLASS and reuse existing background data and cache files, e.g. to just regenerate the plots.

# 1. Extract the model parameters from the passed *.bestfit file

"""
BestFitPlot.py: Extract best-fit parameters from MCMC chains and prepare them for CLASS.

This script:
1. Extracts model parameters from a *.bestfit file (Cobaya output format)
2. Loads the corresponding *.input.yaml file (Cobaya MCMC configuration)
3. Maps Cobaya/MCMC parameter names to CLASS parameter names
4. Combines special parameters (e.g., scalar field parameters) into CLASS-compatible strings
5. Returns a dictionary ready to pass to classy.set()

The resulting CLASS parameters can then be used to:
- Run CLASS with those parameters to obtain scalar field evolution
- Compute scalar field potential and equation of state evolution
- Extract dark matter and energy density evolution
- Generate cosmological plots for analysis

Usage:
    python BestFitPlot.py --bestfit_file path/to/bestfit_file.bestfit [--output_dir path/to/output_dir]

    The bestfit_file should be a .bestfit file generated by the MCMC analysis.
    The output_dir is where the results will be saved (default: current directory).
"""

