def make_pk_plot(
    pk_dataset: Dict[str, Any],
    output_root: str,
    baseline_pk_dataset: Optional[Dict[str, Any]] = None,
) -> Dict[str, str]:
    """Create a publication-quality P(k) plot and save to disk.

    Plots matter power spectrum P(k) vs k in log-log scale.
    """
    k = np.asarray(pk_dataset["k"], dtype=float)
    P_k = np.asarray(pk_dataset["P_k"], dtype=float)

    out_root = Path(output_root)
    out_dir = out_root.parent
    stem = out_root.name

    c_pk = _HIGH_CONTRAST_PALETTE["blue"]
    c_pk_ref = _HIGH_CONTRAST_PALETTE["black"]

    fig, ax = plt.subplots(figsize=(6.4, 4.1))
    ax.loglog(k, P_k, color=c_pk, linewidth=1.85, label=r"$P(k)$ model")

    if baseline_pk_dataset is not None:
        k_ref = np.asarray(baseline_pk_dataset["k"], dtype=float)
        P_k_ref = np.asarray(baseline_pk_dataset["P_k"], dtype=float)
        ax.loglog(
            k_ref,
            P_k_ref,
            color=c_pk_ref,
            linewidth=1.4,
            linestyle=(0, (4.0, 2.1)),
            label=r"$P(k)$ $\Lambda$CDM",
        )

    ax.set_xlabel(r"Wavenumber $k$ [$h/\mathrm{Mpc}$]")
    ax.set_ylabel(r"Matter Power Spectrum $P(k)$ [$(Mpc/h)^3$]")
    ax.grid(True, which="major", alpha=0.32, linewidth=0.6)
    ax.grid(True, which="minor", alpha=0.09, linewidth=0.4)
    ax.legend(loc="lower left", handlelength=2.2, labelspacing=0.35, borderaxespad=0.0)

    fig.tight_layout()
    p_pk = out_dir / f"{stem}_plot4_pk"
    _save_figure_bundle(fig, p_pk)
    plt.close(fig)

    return {
        "plot4_png": str(p_pk.parent / f"{p_pk.name}.png"),
        "plot4_pdf": str(p_pk.parent / f"{p_pk.name}.pdf"),
        "plot4_pgf": str(p_pk.parent / f"{p_pk.name}.pgf"),
    }


def make_cl_plot(
    cl_dataset: Dict[str, Any],
    output_root: str,
    baseline_cl_dataset: Optional[Dict[str, Any]] = None,
) -> Dict[str, str]:
    """Create a publication-quality C_ℓ plot and save to disk.

    Plots scaled lensed C_ℓ (TT, EE, TE) vs ℓ in log-log scale.
    """
    ell = np.asarray(cl_dataset["ell"], dtype=float)
    Cl_TT = np.asarray(cl_dataset["Cl_TT_scaled"], dtype=float)
    Cl_EE = np.asarray(cl_dataset["Cl_EE_scaled"], dtype=float)
    Cl_TE = np.asarray(cl_dataset["Cl_TE_scaled"], dtype=float)

    out_root = Path(output_root)
    out_dir = out_root.parent
    stem = out_root.name

    c_tt = _HIGH_CONTRAST_PALETTE["blue"]
    c_ee = _HIGH_CONTRAST_PALETTE["red"]
    c_te = _HIGH_CONTRAST_PALETTE["teal"]

    ls_primary = "-"

    fig, ax = plt.subplots(figsize=(6.4, 4.1))
    ax.loglog(
        ell, Cl_TT, color=c_tt, linestyle=ls_primary, linewidth=1.85, label=r"$T T$"
    )
    ax.loglog(
        ell, Cl_EE, color=c_ee, linestyle=ls_primary, linewidth=1.85, label=r"$E E$"
    )
    ax.loglog(
        ell, Cl_TE, color=c_te, linestyle=ls_primary, linewidth=1.85, label=r"$T E$"
    )

    if baseline_cl_dataset is not None:
        ell_ref = np.asarray(baseline_cl_dataset["ell"], dtype=float)
        cl_tt_ref = np.asarray(baseline_cl_dataset["Cl_TT_scaled"], dtype=float)
        cl_ee_ref = np.asarray(baseline_cl_dataset["Cl_EE_scaled"], dtype=float)
        cl_te_ref = np.asarray(baseline_cl_dataset["Cl_TE_scaled"], dtype=float)

        ref_ls = (0, (4.0, 2.1))
        ax.loglog(
            ell_ref,
            cl_tt_ref,
            color=c_tt,
            linestyle=ref_ls,
            linewidth=1.25,
            alpha=0.82,
            label=r"$T T$ $\Lambda$CDM",
        )
        ax.loglog(
            ell_ref,
            cl_ee_ref,
            color=c_ee,
            linestyle=ref_ls,
            linewidth=1.25,
            alpha=0.82,
            label=r"$E E$ $\Lambda$CDM",
        )
        ax.loglog(
            ell_ref,
            cl_te_ref,
            color=c_te,
            linestyle=ref_ls,
            linewidth=1.25,
            alpha=0.82,
            label=r"$T E$ $\Lambda$CDM",
        )

    ax.set_xlabel(r"Multipole Moment $\ell$")
    ax.set_ylabel(r"$\ell(\ell+1)/(2\pi)\, C_\ell$ [$\mu K^2$]")
    ax.grid(True, which="major", alpha=0.32, linewidth=0.6)
    ax.grid(True, which="minor", alpha=0.09, linewidth=0.4)
    ax.legend(loc="lower left", handlelength=2.2, labelspacing=0.35, borderaxespad=0.0)

    fig.tight_layout()
    p_cl = out_dir / f"{stem}_plot5_cl_lensed"
    _save_figure_bundle(fig, p_cl)
    plt.close(fig)

    return {
        "plot5_png": str(p_cl.parent / f"{p_cl.name}.png"),
        "plot5_pdf": str(p_cl.parent / f"{p_cl.name}.pdf"),
        "plot5_pgf": str(p_cl.parent / f"{p_cl.name}.pgf"),
    }


